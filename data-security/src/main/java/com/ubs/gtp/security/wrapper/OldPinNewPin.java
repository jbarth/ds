package com.ubs.gtp.security.wrapper;

public class OldPinNewPin {

	private String oldPin;
	private String newPin;

	public String getOld_pin() {
		return oldPin;
	}

	public void setOld_pin(String oldPin) {
		this.oldPin = oldPin;
	}

	public String getNew_pin() {
		return newPin;
	}

	public void setNew_pin(String newPin) {
		this.newPin = newPin;
	}

}
