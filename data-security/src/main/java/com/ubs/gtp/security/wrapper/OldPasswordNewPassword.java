package com.ubs.gtp.security.wrapper;

public class OldPasswordNewPassword {

	private String old_pw;
	private String new_pw;

	public String getOld_pw() {
		return old_pw;
	}

	public void setOld_pw(String oldPassword) {
		this.old_pw = oldPassword;
	}

	public String getNew_pw() {
		return new_pw;
	}

	public void setNew_pw(String newPassword) {
		this.new_pw = newPassword;
	}

}
