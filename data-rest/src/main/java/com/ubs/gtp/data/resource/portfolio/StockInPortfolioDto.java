package com.ubs.gtp.data.resource.portfolio;

/**
 * Dto for sending a stock within a portfolio.
 *
 * @author Joshua.Barth
 * @version since 0.1
 */

public class StockInPortfolioDto {

    private String stockId;
    private float pricePerShare;
    private long numberOfShares;

    public StockInPortfolioDto(String stockId, long numOfShares, float pricePerShare) {
        this.setStockId(stockId);
        this.setPricePerShare(pricePerShare);
        this.setNumberOfShares(numOfShares);
    }

    public String getStockId() {
        return stockId;
    }

    public void setStockId(String ric) {
        this.stockId = ric;
    }

    public float getPricePerShare() {
        return pricePerShare;
    }

    public void setPricePerShare(float value) {
        this.pricePerShare = value;
    }

    public long getNumberOfShares() {
        return numberOfShares;
    }

    public void setNumberOfShares(long numberOfShares) {
        this.numberOfShares = numberOfShares;
    }

}
